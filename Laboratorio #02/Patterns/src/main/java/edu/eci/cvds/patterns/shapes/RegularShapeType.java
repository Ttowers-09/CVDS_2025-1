package edu.eci.cvds.patterns.shapes;

/**
 * Enum representing the types of regular shapes that can be created.
 * Each constant corresponds to a specific regular polygon shape.
 * 
 * The available shape types are:
 * - Triangle
 * - Quadrilateral
 * - Pentagon
 * - Hexagon
 */
public enum RegularShapeType {
    Triangle,      
    Quadrilateral, 
    Pentagon,      
    Hexagon        
}
